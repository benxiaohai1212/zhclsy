							<p>从1956年到1966年底，张茜在中国外交舞台上奔波了整整十年。在这十年中，她出国访问21次，到过苏联、瑞士、印度尼西亚、越南、柬埔寨、缅甸、巴基斯坦、印度、尼泊尔、锡兰（斯里兰卡）和阿富汗。算上后来成立的孟加拉国共12个国家。</p>
							<p>在国内，张茜所接待过的总统夫人、王后、公主、首相夫人、总理夫人、部长夫人和妇女界人士等各种身份的外宾不计其数，在中国的外交事业上，特别是在和东南亚国家的友好往来中，都留下了自己的名字。很多人称张茜为新中国外交战线上的夫人“形象代表”，的确，称她为新中国第一代女外交家并不为过。这也正是这本画册副题的由来。因陈毅曾在诗中把张茜亲切地比为“红芳”，故这本画册定名为《红芳影录》。</p>
