							<p>今年6月，陈毅元帅夫人张茜诞辰91周年之际，陈毅和张茜的第二子陈丹淮编著的画册《红芳影录——纪念新中国女外交家张茜》由我社正式出版。</p>
							<p>画册中的300余张旧照，绝大部分未曾发表过。这些照片不仅体现了张茜的美丽、端庄，陈毅、张茜的夫妻情深，子女对母亲的深厚怀念，同时也向我们展示了一道独特的风景——新中国外交史上不曾浓重渲染却格外绚丽多彩的“夫人外交”。</p>
