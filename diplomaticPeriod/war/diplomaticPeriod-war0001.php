							<p>最早听说陈毅的大名，是我当学生的时候，没想到后来竟有幸认识他，并能近距离的向他学习。陈毅同志给我的印象非常深。政治家、军事家、外交家等方面，大家都讲得非常好。我仅讲讲同他接触中，特别是陪少奇、陈毅1963、1966年两次出访中，【包括印尼、缅甸（两次）、巴基斯坦、柬埔寨、越南、孟加拉等国】直接体会到他的为人。陈毅同志待人热情真诚直率，又非常风趣，一点架子都没有。但，很有气魄。陈毅考虑问题很细致周到。记得1966年外委会同志我陪少奇出访巴基斯坦等国，我因正在河北省农村搞四清（社教工作），曾正式向组织请假。回京为少奇作出国准备时，陈毅同志直接对我说：你这次不去，将来如哪国单独请你，会更麻烦。我认为陈毅同志讲的有道理，就服从了安排。</p>
							<p>在怀念陈毅同志的时候，我还想提提张茜同志，她给我的印象也非常好。张茜同志担任过中央组建的夫人工作领导小组组长，主要是负责夫人涉外事务。她聪明好学，多才多艺，对子女要求严格、对自己也很严格，如：为了节约，外事活动多，服装费不足，自己常拼拼改改，既节约，又体面。我们是好朋友。</p>
							<p>今天，我们共同怀念陈毅同志，他永远是我尊敬和学习的榜样！</p>
