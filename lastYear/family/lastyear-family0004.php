							<p>编者按：3月20日是张茜同志逝世30周年，为了纪念这位杰出的女性，她的儿媳秦昭同志写了一篇诗体散文，以此来纪念亲人。</p>
							<p>那么年轻你就进入历史，你把美丽端庄永远留在人们的记忆之中。你是新四军的一名女兵，祖国江南记录着你青春激情的火焰和光荣。你是外交部长的夫人，新中国外交留下你秀丽的倩影和面容。高雅的气质风度使你魅力无穷，温文尔雅的举止言谈让一代人称颂。在国际舞台上，你为中国妇女打开了一片璀璨的星空，在对外交往中，你为伟大的祖国增添了一道绚丽的彩虹。</p>
							<p>一般人只知道你美丽，不知道美丽中蕴含着丰富的智慧和才艺。一般人只以为你柔弱，不知道外表柔弱的你，内心却是如此的坚毅。一般人只认为你娇贵，不知道这娇贵经历过风霜和磨砺。</p>
							<p>翻阅你圈点过的诗词辞赋，处处沁透着你的血汗心路。打开你抄录的春蓝词谱，那隽永的草书是你美丽人生的画图。刻苦用功不断进取是你一生始终坚持的道路，尽管前面有荆棘坎坷，有艰难险阻。学习外语你很早就开始起步，无论环境多么险恶，条件多么艰苦。你翻译的俄文小说，也是你呕心沥血勤奋学习的叙述。你的执著，你的坚持开始有了收获，幼苗长成了茂密的云树。</p>
							<p>你鄙视轻浮虚伪，不求进取，荒废时光。你做事认真严格，珍惜寸金，自律自强。你毕生追求真理，不断实现着美好的理想。你短暂的生命火花，光彩照人，熠熠辉煌。你出众的才华，是子孙的榜样，你广博的学识，成为儿女的师长。你人生的征程，是我们前进的方向，你无言的教诲，时刻铭记在我们心上。</p>
