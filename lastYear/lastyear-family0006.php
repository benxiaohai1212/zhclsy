							<h1>孩童眼中的张茜</h1>
							<div class="parameters"></div>
						</div>
						<div class="txtContent">
							<p>我的母亲张静是张茜的叔伯姑姑。从幼年时代开始，她们之间的关系就很要好，这种关系一直维系着。正是因为这种关系，我从小有幸与陈毅元帅进行面对面的交流。</p>
							<p>记得文革时期，陈老总受到了冲击，被下放到了石家庄，张茜也一起去了。每次回到北京时，她总是要到我家里来坐一坐，有时就住在我家。而且走的时候，经常是我送她到公交车站。她很朴实，冬天里总是穿着军大衣，带着口罩挤公交车，防止外人认出来。后来陈老总生病了，她很焦虑，母亲一再地安慰她，当时，都以为手术后就会好的。可是，谁也没有想到，最后的结果竟然是癌症晚期……</p>
							<p>小时候，张茜给我留下的印象是，朴实、严谨、乐观、通达，处处为他人着想。记得上世纪60年代末的某一天，由于当时的政治局势很紧张，外人进入中南海时审查比以往更加严格了。为了不给陈老总添麻烦，我母亲和张茜约好在北海公园的大门口见面。由于我和母亲迟到了，张茜在那儿等了将近1个小时。见面后，她毫无怨言，反而再次邀请我们到家里去坐一坐。为了不打扰陈老总，我母亲决定，不进中南海。于是，那一天我们在北海公园转了好几个小时。</p>
							<p>1971年的下半年，陈老总终于可以回北京治病了，他们全家搬出了中南海，搬进了前永康胡同。这时我们两家的来往少了约束，走得更近了。每次陈老总从医院回家的时候，我都会和母亲一起去探望。</p>
							<p>可是就在1972年1月6日的深夜，陈老总因为病魔离开了人世。此时，张茜悲痛地对身边的人说：“别惊醒她们（指我母亲），到凌晨再打电话吧。”</p>
							<p>1972年春节的时候，我和母亲去看望张茜。张茜问我，春节年货的供应是多少？我说，花生每人半斤，瓜子每人四两。听后她很高兴地说：“街道还很照顾我们，给了我们每人半斤瓜子呢。”几十年过去了，每当我想起这件事，都感到很心酸。一位元帅夫人，在那艰难的时刻，却是如此地豁达、乐观。</p>
							<p>陈老总去世后，没有多久就查出张茜也患了癌症。然而她竟是凭着顽强的意志与病魔斗争：一面配合医生积极治疗，一面要完成整理出版陈老总诗词的心愿，还要支撑着这个家。</p>
							<p>1972年和1973年的暑期，张茜在北戴河养病期间，我也有幸被邀请去了北戴河。当时的我只有15、16岁，不太懂事，只记得张茜每天都在整理材料，看书，写东西。有一天，稿纸没有了，她就亲自到街上的服务部去买稿纸。</p>
							<p>小的时候，我的身体比较虚弱，张茜尽管身患癌症，却一直很关心我，要我多吃饭，多锻炼身体。有一次，在问到我的功课时，我说90多分，她感到很满意。后来，她还鼓励我要好好学习英语，遗憾的是，后来，我没有做到，辜负了她的希望。70年代初，每当有文艺演出，可以带家属时，张茜总愿带着我去，希望我能多长见识。她是一个事事为别人着想，又严格要求自己的人。</p>
							<p>38年过去了，张茜的音容笑貌总是在我的脑海里闪过，她的美是难以用语言形容的，她的气质是无法复制的。愿她在天堂安息，永远记住我这个小妹妹。</p>
							
