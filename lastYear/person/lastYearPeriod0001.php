						　　<p>又是一阵响动，小丹发现妈妈的神色已经平静下来，她从沙发靠椅中站起身，把手杖放回到门边原来的位置。又转身脱下笨重的大衣，也挂回原处，。然后，她用手习惯地理了理散乱的短发，象指挥员下达命令似的以坚定，从容的口吻，对站在身边的小丹说：</p>    
							<p>“我们要重新安排我们的生活！”</p>
							<p>声音不大，却充满自信和力量。</p>
							<p>方才妈妈还沉浸在往事的回忆中，痛不欲生，现在她已战胜了悲痛，擦干了眼泪，象迎接战斗的战士那样，站到了自己岗位止。一股强大的热流在小丹的心中喷涌：“妈妈，我的好妈妈，你还和过去一样坚强！”他再也控制不住潸潸而下的泪水，呜呜咽咽放声痛哭起来，就象一个刚从恶梦中惊醒的孩子……</p>    
							<p>遗体从日坛医院病房移到301总院的太平间，狭小的房间，狭窄的坡梯走道，这就是陈毅元帅遗体告别的场所。</p>
							<p>追悼会会场设在八宝山烈土公墓挂满灰尘、蛛网的小礼堂里，一架破旧的留声机代替了哀乐乐队。参加追悼会的人数原来限定五百人。追悼会由总政主任主持，由军委副主席致悼词，这就是副总理兼外长陈毅元帅葬礼的规格。</p>
							<p>悼词只有六百字，其中关于陈毅革命近五十年所历任的职务罗列，就占去三百多字，这就是对开国元勋陈毅的评价。</p>
							<p>张茜用冷峻的眼光注视着这一切。她看得出有一些心怀叵测的人，在千方百计的企图缩小、贬低陈毅在人们心目中的影响。陈毅生前，他们诬陷他，中伤他；死后，他们还要把他打入另册。当工作人员拿着、悼词讲稿来征求意见时，张茜坚定而淡然地回答说：</p>    
							<p>“我只要写上陈毅的一生是‘为人民服务的一生’八个字就行了，其余随便你们怎么写！”</p>   
							<p>一月十日下午，毛泽东主席突然来到八宝山烈士公墓礼堂，执意戴上黑纱，出席陈毅元帅的追悼会，并在休息室里，接见了张茜和四个孩子，对陈毅同志的一生作了很高的评价。</p>
							<p>张茜这时哭了，她紧紧抱着鲜红党旗覆盖着的陈毅元帅骨灰盒放声痛哭，高声地呼唤：  “仲弘，毛主席来参加您的追悼会了，他老人家说，‘陈毅是个好同志’还对你每个革命历史时期，都作了很高的评价，你听见了吗？你可以瞑目了！”</p>
							<p>张茜又是彻夜未眠。毛主席的声音在她耳边回荡。她一字一句地追记着毛主席的谈话内容，准备把主席的讲话整理出来，一份交给党中央，一份让孩子们留存，盼望毛主席谈话的原文能尽快公布于世，以领袖的公正评价，来洗刷“文革”以来对陈毅的一切诽谤，污蔑之辞。</p>
							<p>一盼再盼，谈话记录送上后如石沉大海，杏无回音，却从上海传来了张春桥恶狠狠阴森森的讲话：不要以为毛主席参加了陈毅的追悼会，陈毅就没有错误了。毛主席历来是对亲者严，疏者宽。</p>
							<p>听到这个所谓讲话，张茜气得浑身发抖。她心里更加明白了，只要中央一天还有这些人在，毛主席对陈毅的评价是不会有见天之目的！她是多么急切地盼望着云消雾散的日子快些到来呀！</p>
							<p>陈毅元帅逝世不到两个月，张茜就被确诊为晚期肺癌。叶剑英同志到医院看望她。她听说叶帅还亲自参加了对自己的病情会诊，预感到自己的病情严重，她要求叶帅毫不隐瞒地把真实情况告诉她。叶帅如实讲述了医生的诊断结果。</p>
							<p>张茜听完，坦然一笑，说：“发现了癌细胞，这没什么了不起，顶多就是跟陈老总走了吧!不过，现在我不能死，我还有重要的工作没有做。”</p>
							<p>张茜让大儿子小侉把陈毅的全部诗稿、诗稿背景材料和有关的历史资料统统收集起来。张茜开始着手整理陈毅诗稿的工作。</p>
							<p>张茜手术后，在疗养期间，她曾对原新四军自己的一位挚友说道，“我希望我的身体能很快恢复起来，我要用我在世界上存活的全部时间，来整理陈老总的诗集。陈老总的诗，真实反映了他一生的革命经历，是他用鲜血和生命写成的。我要用这个事实，说明陈老总一生对革命事业的贡献，说明他是党的忠诚战士，是人民的忠实儿子。我要把这个工作干完，一直干到自己咽最后一口气！”</p>
							<p>张茜知道，属于自己生命的日子不太长了，她无法抗拒癌症的威胁，可她能扩大自己有限的生命。她白天不停手地抄呀写呀，每到夜深入静，她都要悄悄起床，披上那件她用陈毅穿过的礼服亲手改制的上衣，继续白天的工作。那洁白可体的华达呢上衣，陪伴着她，给她温暖，穿着它，就仿佛仲弘仍然在她身旁，她精力充沛，思路畅达，伏在桌上看呀，写呀，总不知倦怠！</p>
							<p>七四年一月，张茜在赵朴初老人的具体指教下，编完了一本陈毅诗词选，当时没有哪个出版社敢于发排，张茜决然地说：“散发到老百姓手里。”于是，火种点燃了，人民群伞用铅印、油印、复写等等办法，把陈毅那一首首用血与火凝炼而成的诗章，在祖国大地上传开了，在“四人帮”猖獗的苦难日子里燃烧着。几万只手一起传抄、印刷，版本、印数无法统计。陈毅是人民忠实的儿子，他的诗属于人民。陈毅的诗在战斗中诞生，以战斗的方式传播，鼓舞着亿万人民去迎接新的战斗！</p>
							<p>读到从全国各地寄来的慰问信、读后感，张茜憔悴的脸上，浮现出快慰的神情。她并没有停笔，又开始精心整理陈毅的文稿。</p>
							<p>肺癌细胞不断增加，广泛转移，张茜终日连连呛咳，大口吐血。</p>
							<p>一天，四个孩子约好探病时间，一起走进妈妈的病房。张茜温和地看看这个，看看那个：</p>
							<p>“怎么，你们都回来，为我送终了？”</p>
							<p>孩子们劝妈妈注意休息，要珍惜自己的身体。</p>
							<p>张茜望着眼含热泪的孩子，欣慰地笑了：“不要为我担心，不整理完你们父亲的诗稿和日记，我是不会死的！”</p>
							<p>夜深了，小女儿姗姗还在张茜病房门外徘徊，妈妈每一阵咳嗽，都象撕裂着她的心。她的寒假已经结束，按规定明天就要启程返回英国。</p>
							<p>她是两年前被组织派往英国学习的，当时妈妈就要作手术，她不放心。可是妈妈坚持让她走：“送你出国继续学习外语，这也是你爸爸生前的愿望。过去几年耽误了，没有学到什么东西，现在应该抓紧时间好好学习，这样才对得起你的父亲。”</p>
							<p>可是现在，妈妈病势沉重，作为妈妈唯一的女儿，她不能守在身边照顾妈妈，实在不忍心。</p>
							<p>“妈妈，我想多留几天，好好照顾你！”</p>
							<p>听了姗姗的话，张茜眼窝一热，她完全理解女儿对她的一片心，但她还是强笑着说：  “学习重要，你还是按时回去学习吧！我这里有你哥哥、嫂嫂照顾，你完全可以放心。我要亲眼看到你的学业成就，就是死了，也好带话给你爸爸，让他高兴！”</p>
							<p>姗姗按期返回英国了。春节时，妈妈让哥哥代笔给姗姗写了信，寄去了贺年片。妈妈亲手挑选的精美的贺年片，给远在英国的姗姗带来难以形容的欢快。可她那里知道，妈妈生命垂危，已到了最后时刻！</p>
							<p>“打电报让妹妹回来看看您，好吗？”</p>
							<p>张茜流泪了。作为母亲，谁不会思念远在万里之外的女儿？谁不愿意在告别这个世界之前，合家老小再团聚一次?！</p>
							<p>但是，张茜还是困难地摇摇头，吃力地，一字一顿地说：</p>
							<p>“不用叫姗姗回来，她耽误的时间太多了，再说，为了护理爸爸，她心灵受到的创伤够深的了，不要让她再为我难过吧！”</p>
							<p>张茜就这样静静地告别了人世。</p>
							<p>她生前曾经立志作一个平凡的人：活着，就刻苦地学习，勤奋地工作，把自己的心血默默地、一点一滴地全部献给党和人民，献给伟大壮丽的事业，要离开人世时也尽量不惊扰旁人，不给别人（包括自己的子女）增加痛苦和不必要的负担。现在，她实践了自己生前的誓愿。</p>
							<p>她是一个好妻子。为了完成党交给的光荣任务，她尽心竭力协助丈夫工作，牺牲了自己的爱好、志愿，献出了自己的青春、健康和整个生命。</p>
							<p>她是一个好妈妈。为了让下一代能肩负起先辈遗留下的历史重任，她把伟大的母爱溶汇在严肃的、一丝不苟而又耐心的管教之中，象辛勤的园丁一样培育着一株株幼苗茁壮成长。</p>
							<p>她是个好战士。她象千千万万拿枪的战士一样，日夜坚守在自己岗位上，履行着自己的职责。敌人的枪炮刺刀和阴谋家们的诽谤中伤，都不能使她后退一步！</p>
							<p>她是一个普通的共产党员，但到她灵前凭吊的人，却成百上千。这反映了人们对陈毅元帅的崇敬，表达了人们对张茜同志深深的尊敬与怀念。</p>
							<p>人们怀念她，因为她是中国女性的优秀代表，她所走过的道路是千万中国妇女所走过的共同道路。</p>
							<p>人们怀念她，因为在中国漫长曲折的革命斗争中，需要成千上万象张茜同志这样的勇敢，坚贞、正直、聪慧的伟大女性</p>
