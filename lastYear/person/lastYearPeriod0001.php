							<h1>陈毅元帅的妻子</h1>
<h2>—— 献给一位伟大的女性</h2>
							<div class="parameters"></div>
						</div>
						<div class="txtContent">
							<p>夜归已十一时，春蓝与我谈黄花塘往事。颇凄苦。总结结缡十余年，我甚得她的帮助。家有贤妻，男儿不道忿事。此语信然。她近来工作方面已上轨道。休息太少，健康可虑也。</p>
							<div style="text-align: right;padding-right:200px;">五四年四月一日夜<br/>陈毅</div>
							<p>寒风凛冽，枯枝颤栗，清冷的月光笼罩着冰封的大地。</p>
							<p>夜深入静，小巷空寂，巨大的悲痛降临在北京前永康胡同9号小院。 一个小时前——1972年1月6日23点55分——举世闻名，全国亿万人民敬爱的陈毅元帅永远闭上了双目，在北京肿瘤医院的一间普通的旧病房里溘然长逝了。</p>
							<p>铅灰色的铁门无声地打开，一辆红旗牌轿车缓缓地驶进小院，在楼门前稳稳停住。身着素服的张茜慢慢从车上下来，走向屋门。她径直来到丈夫生前办公室兼卧室门口，拉开灯，默默地坐进一只旧沙发，浮肿的眼睛凝视着门边。许久许久，没有一丝声响。</p>
							<p>两个身材魁梧的青年军人静静守护在张茜身旁。脸圆些的是二儿子小丹，脸稍长的是三儿子小羊。他们目不转睛地注视着无言的妈妈，釉交换着忧虑、不安的目光。妈妈长久痛苦的沉默使他们感到难以忍受。他们宁愿妈妈象天下大多数失去丈夫的妻子一样，流泪千行，痛哭失声，以泄胸中积淤的苦衷，把巨大的悲痛强抑在心底，对妈妈已很虚弱的身体会带来难以预料的伤害。他们已经失去了父亲，不能再失去母亲！</p>
							<p>可是，脸色苍白的张茜，几乎一直没流泪，更没有抽泣和痛哭，她挺直腰杆坐在沙发里，干涩的眼睛凝视着前方，一动不动。以无言的沉默，石雕般的冷峻，承受着扑面压过来的一切痛苦……</p>
							<p>自从陈毅第一次手术之后，张茜开始参加了医生组织的每一次会诊，她曾对医治好丈夫的病充满了热忱和希望。但是，自从陈毅元帅在中央组织的老同志座谈会上，以极大的义愤，作了揭发林彪叛党罪行的长篇发言，在会场上猝然倒下之后，虽经过了第二次手术，病情仍日趋沉重。张茜心里已经完全清楚，她和陈毅元帅夫妻永诀的日子步步逼近了。深重的忧虑和长期住在医院陪护丈夫的辛劳，巳使她身体很衰弱，痰中经常带血。儿子催促她住院检查治疗，她总摇头，说：“只要你爸爸的病能控制住，我怎么样都无所谓。”</p>
							<p>　　直到医校毕业的小女儿姗姗被调来参加陈毅的特护，张茜才答应住院。姗姗第一次出现在张茜的病房门口，就被妈妈发现了，她眼睛一亮，不能自抑地向女儿伸出双臂，紧紧地把姗姗搂抱在怀里，许久，她才虚弱地说出一句话：“姗姗，来帮帮妈妈吧！”</p>
							<p>　　这是一位坚强的母亲向自己的女儿发出的求援呼唤，声音虽然微弱，却强烈地震撼了姗姗的心。她“哎”了一声，便猛地把脸深深埋进了妈妈的怀抱，泪如雨下。姗姗从妈妈充满忧虑的脸上，已看到笼罩在爸爸头顶的阴影……</p>
							<p>陈毅缠绵病榻月余，及至一九七二年元旦刚过。</p>
							<p>　　这是我近六年来收到的两万余封来信中最“光明正大”的一封，不但标明年龄，还写了一个你永远不会找到那个“前明”的地址！</p>
							<p>笔迹倒象是个十九岁的人，然而信的内容和情绪，包括用词，三十九岁以下的人是绝对写不出来的。  我不相信一个十九岁的中国人会对他诞生前九年起遭难的“右派一群”怀有如此炽烈的仇恨。</p>
							<p>我的手是不需要洗的，因而也不需要骂人和“唾沫星子”，倒是想间一问写信人：阁下的手若没有染上某种洗不掉的东西，何至于满腹怨恨，对于几年来除政治凶手、歹徒和经济罪犯外未攻击过任何人的作者“一肚子火儿没处撒，只好骂人出气”呢？</p>
							<p>很好的一份生日礼物。信上的唾沫星子，就是我几年来工作效益的佐证。哈，他们发狂了！快哉！</p>
							<p>还没有接到过这样的邀请。山西省原平县的四位县委书记——吕日周、王登昌、赵生才、张存寿同志联名来信，请我到这个山西省改革的试点县“走一走，看一看，到改革的激流漩涡中体验一下生活”。非常欣慰！</p>
							<p>原来他们也是我的读者。</p>
							<p>附寄若干材料。经济改革的显著效益是振奋人心的。全国很多报刊已经报道，那么我还有什么可写呢？有。信中所写一年多来“经过尖锐复杂的斗争，终于冲破‘左’的束缚，实行了一系列改革……”这个过程，还没有充分写出来。几位书记自觉地改变当地干部、群众传统意识形态的工作所取得的成果，我也很感兴趣。还有，那个县的纪检委，是出席全国纪检工作会议的唯一一个县级单位，一定也有它独特的经验。</p>
							<p>和宋秀兰会面，至少也是第四次了。她从七十年代起就和大兴安岭的贪污盗窃分子、以权谋私的干部进行斗争。一九八一年《黑龙江日报》曾以头版头条地位表彰过她的事迹。一九八三年夏，《黑龙江日报》又发表了她遭到商业局科长殴打的消息（她挨打，远不是第一次了）。现在，她仍在继续她的马拉松斗争。</p>
							<p>真正是可歌可泣！今天得知去年五月间胡耀邦同志在她的申诉、揭发信上作了很长的批示。黑龙江省纪委派出调查组查了七十余天，尚未上报。宋秀兰对那次调查是不满意的。我问她：去年来过的加格达奇木材部门的女会计刘显筠的情况怎样？答曰：“她揭发出来的大贪污犯张杰（女）已拘留将近一年，至今未开庭审理，不断有人说傅。刘显筠本人现在正在受折磨：不给她工作，还得上班，坐在那里受人挖苦讽刺。精神已接近分裂。”</p>
							<p>正在整理行装，准备中午去石家庄，听到有人叫门。朱洪赶忙去迎住，告以我实在没有时间。听话音，是一位年轻的女性。后来，朱洪又告诉我，那两位来客还在大门口等着，我去报社时要小心。</p>
							<p>实在是没有时间。两个小时里，我要为安装电话问题再写一次信（由于只能由别人传话，把约定时间搞错，我和两位美国记者在星期二、三分别在建国饭店空等了对方一个多小时）。找群众工作部徐放同志谈甘肃问题。找记者部领导谈湖南省南县问题。找报社领导谈应邀访问西德和美国的问题。抄写党员登记表。查找河北省读者来信。找张平力谈山东省姜怀玉案调查材料的印刷问题。给孔罗荪同志打电话谈郭芸泓的工作问题……</p>
							<p>仍然未作到轻装——必须带走的材料太多，背包和提包都很重。时间很紧了。不料还未出大门，又遇到陕西铜山矿务局的卫生员李巧环。只能一面走一面谈了。再次劝她放弃上访。她说又发现了那里一些新的问题。我说算了，你都三十岁了，铜川不得归，就先到别处找个工作暂时栖身，以后再告吧。我不是介绍你去找郑秉琦同志了吗？那是个好人，她也说是，见过，待她很热情。我说那好，去吧，别上访了。她表示同意。我上了公共汽车，却并不放心。上次她也答应过的，不久又连续寄来两份打印得很精致的控告材料。</p>
							<p>在305次列车上看今早来客留下的材料，心情不禁为之黯然。我应该见见她的。又是那个“四人帮”后期以“教育改革”闻名于世的那个朝阳市！</p>
							<p>这是一份渗透了血和泪的控诉书。王巨是大学毕业生，事件发生地是文化局，时间是距二十世纪末不到十五年的今天。王巨犯下两个错误：一，她不肯作局长和局长夫人——她顶头上司的儿媳，二，她不肯介绍那位夫人入党。还有第三个错误——她不该向自己视为神明的父亲坦露自己的苦闷和真实思想，也不应把其父依据古代定理书写的大批判文件保存下来，以致被局长夫人依权取走，翻印和散发，成为她的思想罪状！她个人生活中的不幸也被广为传播，成了中伤她的手段。这一切都是咎由自取，谁让你遭到不幸呢！</p>
							<p>只有两种可能：王巨诬告朝阳市文化局长夫妇，构成诽谤罪，应依法惩处，或者，文化局长夫妇违反共和国宪法（去年是第一部宪法诞生三十周年！），严重侵犯了王巨的人身权利。怪哉，难道朝阳市没有法院吗？他更加衰弱，多次昏迷。</p>
							<p>二日深夜，周恩来总理接到医生报告。陈毅同志现在神智非常清醒，似是回光返照迹象。周总理立即从人大会堂乘车赶到日坛医院，快步走进陈毅病房……</p>
							<p>五日上午，陈毅嚅动嘴唇问了他一生中最后一句话：“叶帅来了没有？”不巧，叶帅刚刚来过。他一直在走廊里徘徊等待了很久，陈毅却一直昏睡不醒。找叶帅的电话还没要通，陈毅又一次陷入昏迷之中。</p>
							<p>六日下午四时，经过医生抢救，陈毅跚刚恢复了自动呼吸。他神智略微清醒，生命细如游丝。闻讯匆匆赶到的叶剑英，望着生命垂危的老战友，泪流语塞。他颤抖着双手从大衣口袋里摸索出一张纸，上面抄着毛主席为所谓“二月逆流”平反的一段话，交给守坐在陈毅床头的姗姗，让她念给爸爸听。</p>
							<p>叶帅这样做，并非受谁之托，他所期望的只是让患难与共的老战友离开这个世界之前，知道这个来得太晚的“甄别”，让老战友一生中，尤其是晚年备受创伤的心灵，得到一丝安慰。</p>
							<p>立在床边的张茜被叶帅真挚的友情深深打动了，她立即催促女儿：“快，快读给爸爸听！”</p>
							<p>屋里回响着姗姗缓慢、清晰的声音。张茜眼里渐渐涌出泪光，她依在陈毅病床边，尽力克制着自己内心的激情，专注地凝视着丈夫开始暗淡的双目。当姗姗读完全文，已经无力说话的陈毅，依照事先的约定，眨了眨眼睛，表示他听见了。张茜紧咬着嘴唇，激动得周身发颤，一边不停揩擦面颊上的泪水，向陈毅强作微笑，一边催促着女儿再读一遍。没有什么人比她更熟悉自己的丈夫：他对个人委屈、荣辱看得很轻，而对真理的信仰和追求却是无比坚定和执着的。那个“甄别”尽管姗姗来迟，但总是透出了一点真理的声音。</p>
							<p>突然，陈毅又一次陷入了昏迷，医生立即聚拢过来，开始了又一次紧张的抢救。张茜始终坐在离床不远的沙发上，焦急不安地注视着抢救工作的进行。</p>
							<p>深夜十点多，银发斑斑的吴恒兴院长和李冰副院长坚持把张茜同志请到外面休息室，再三劝慰她注意自己的身体，说陈总的抢救工作由他们全力负责。</p>
							<p>张茜终于走出病房。因为吴院长，李副院长是完全可以信赖的，而她自己头晕目眩，浑身冷汗，也确实支撑不住了。</p>
							<p>张茜一出门，医生立即根据吴院长指示，对陈毅采取了人工挤压心脏的最后抢救措施。姗姗顿时明白了，决定爸爸生命的关键时刻到了！吴院长请妈妈出去，是担心她经受不住对亲人采取那种强制措施的刺激。她高度紧张地注视着双目紧闭的父亲，忘记了时间的流逝，更不知道隔壁休息室里发生的一切。</p>
							<p>吴恒兴院长从陈毅病房出来，摘下口罩，缓慢沉重地走到隔壁休息室里。一碰上张茜急切的询问目光，老人嘴唇颤抖了，强忍住的泪水在眼眶中闪光，他轻轻地说：“心脏按摩已经一个半小时了，仍然没有心跳……”</p>
							<p>瞬间，隔壁房基一阵凝固似的寂静。但很快搬动桌椅的碰撞声，氧气瓶胶轮的移动声和挂水铁兜落地的空咚声，从隔壁传来，打破了病房的安静。这时，李冰副院长进采低声通知说，遗体告别可以开始了。</p>
							<p>张茜拒绝别人搀扶，站起身，向陈毅的病房走去。孩子们紧紧跟在她的身后。进了房门，张茜和孩子们都怔住了：顷刻之间，病房已分为天上人间！不久前这儿还是整洁、朴素、舒适、温暖的病房，现在窗门大开，室内家俱全部撤去，四壁空空，变成了肃穆清冷的向遗体告别的场所。借大的房闻里只剩下一张病床，已经换上普通军装的陈毅元帅，眉头略耸，双目紧闭，安睡在病榻上……一种悲惨凄凉的气氛笼罩着整个病房，令人感到压抑、窒息。</p>
							<p>突然，张茜身旁的吴院长双手捂住脸，掉头向门外跑去。紧接着，隔壁传来这位华侨老专家痛不欲生的嚎啕大哭，哭声中还夹带着断断续续的呼唤声：</p>
							<p>“为什么不早点找我来给陈老总会诊哟!……为什么？为什么？为什么到了大火烧到房顶才来找我？……陈老总，你本来是不会这样快死，不会的！你应该活着，中国需要你，我们需要你呀！”</p>
							<p>大家担心这种悲凉的气氛给张茜刺激太大，都劝她回家去。她摇摇头，又向床头靠靠，伏下身子细细端详着陈毅的遗容，好象在仔细辨认亲人脸上每一个细小的标志。她在心里说，“仲弘，你怎么走得这样匆忙？前些天你还跟我开玩笑。连续十六小时给你输液，你还笑着说：‘天下哪有这么长的宴席？！'你不能进食，不能排尿，四肢和鼻子里插上了各种导管，你见我流泪，还笑着安慰我，‘不要担心，你瞧，我都管道化了，这不是更省气力了？！，哎，仲弘，你吃了大苦了……”</p>
							<p>一直陪着抢救陈毅的剧院长李冰，淌着泪把张茜扶出病房。他知道，这种无泪的痛苦，比泪流千行更伤人身体，更易积郁成疾啊！</p>
							<p>恐怕是桌上闹钟的滴嗒声，把张茜从遐想与追忆中唤醒，她抬起头，对身边的亲人说，  “夜很深了，你们都回屋休息去吧！”语调平静却毫无商量的余地。</p>
							<p>“妈妈您也……”</p>
							<p>“我想一个人在这里呆一会。”</p>
							<p>大家都顺从地离开了房间。小丹站在门口等关门，在大家静静地走出房门的瞬间，他分明看见一直目送大家出门的妈妈，脸上浮现出一种异样的神情：妈妈想起了什么？他一时琢磨不透。</p>
							<p>张茜望着正离开房间的体格魁梧的儿子和文雅体贴人的媳妇，心里确实涌起一阵宽慰的微波，这是巨大悲痛之余的一丝宽慰。她闭了灯，在黑影里又开始在心里向安睡在那间冰冷房间里的丈夫，倾诉着能使他宽心的话，</p>
