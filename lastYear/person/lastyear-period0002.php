<h3>題《海思圖》</h3>
　　——緬懷陳毅元帥<br/>
張伯駒
<ul>
<li>痛我長城坏，寒天落大星。</li>
<li>遺言猶感激，老淚忽縱橫。</li>
<li>曰暗旌旗色，江沉鼓角聲。</li>
<li>東南餘壁壘，忍遇亞夫營！</li>
</ul>
