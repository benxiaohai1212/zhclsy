<div class="txtpoetry">
<h3>陳毅同志逝世二周年</h3>
李一氓
<ul>
<li>橫槊遺詩韻末終，蒼鷹黃犬恨無窮。</li>
<li>忍看兒女幾行淚，灑向千年五棵松。</li>
<li>沉江鐵索斬干尋，赤幟高懸人秣陵。</li>
<li>撒手人天恩怨在，六龍飛處動哀音。</li>
</ul>
</div>
<div class="txtpoetry">
<h3>陳毅同志輓詩</h3>
趟樸初
<ul>
<li>殊勳炳世間，直聲滿天下。 </li>
<li>剛腸忌鬼域，迅雷發叱咤。</li>
<li>賴有堯日護，免跖唔斧伐。</li>
<li>眾望方喁喁，何期大樹拔？</li>
<li>豈徒知己感，百年一席話。</li>
<li>慟哭非為私，風雨黯華夏。</li>
</ul>
</div>
