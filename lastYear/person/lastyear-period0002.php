<h3>輓陳毅同志</h3>
董必武
<ul>
<li>久被病魔折，元良竟喪生。</li>
<li>立功半不伐，求藝廣多成。</li>
<li>皓月無幽意，清風有激情。</li>
<li>井崗山上樹，瞻仰總心傾。</li>
<li>&nbsp;</li>
<li>聞君病重久，欲探未成行。</li>
<li>憶昔比鄰住，曾為倒屣迎。 </li>
