<h3>金縷曲</h3>
—— 讀陳毅元帥詩詞<br/>
張璋
<p style="text-align:left;">一片豪情吐。記當年，半生戎馬，半生筆楮。《梅嶺三章》驚天地，陰府閻羅何懼！平生志、剛腸金鑄。轉戰江南齊魯地，最關情、巧奪孟良崮。踏淮海，大江渡。奔騰萬里晨昏路。別淞滬，風雲際會，周族寰宇。文略武韜經綸手，內政外交功著。胸坦蕩、翩翩風度。一代風流人去也；縱千言萬語情難訴。揮此筆，為君賦。
</p>
