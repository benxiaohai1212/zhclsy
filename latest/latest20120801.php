							<p>2012年6月11日上午，北京新四军暨华中抗日根据地研究会、中国-柬埔寨友好协会在全国人民对外友好协会举行《纪念新四军女战士、女外交家张茜诞辰90周年座谈会》。参加座谈会的有：全国对外友好协会和北京新四军研究会领导、新四军女战士、外交部老干部、国际共产主义战士马海德医生夫人和米勒医生夫人、张茜的子女及亲属等。在座谈会上，北京新四军研究会和全国友协领导、外交部老干部、马海德医生的夫人深情回顾了张茜同志的生平事迹。《三个新四军女兵的多彩人生》一书的两位作者讲述了创作过程及从中受到的启迪。张茜同志的子女和亲属也讲述了她作为母亲和姐妹的感人故事。张茜同志的第三代还朗诵了她的诗作《花环歌赞》并合唱《新四军军歌》，以表达对革命前辈的敬仰之情。</p>
							<p>张茜同志不仅是原国家领导人陈毅元帅的夫人，而且是新四军女战士、俄文翻译工作者、外交家和国际社会活动家。1922年6月11日张茜生于湖北省汉阳。1938年加入新四军。1940年加入中国共产党。战争年代，她历任新四军战地服务团团员、新四军江南指挥部政治部宣传干事、抗日军政大学华中分校宣传干事、新四军卫生部政治指导员、华中建设大学财经系宣传干事、山东野战军直属队副协理员、白求恩医学院宣教科副科长。解放后，她历任上海俄文专科学校宣教股股长、上海新文艺出版社编辑、北京人民文学出版社和对外文化联络委员会编译，她以耿星为笔名翻译出版了苏联作家绥拉菲摩维奇的小说集《沙原》和李昂诺夫的多幕剧本《平平常常的人》。</p>
<p>陈毅任国务院副总理兼外长后，张茜同志也逐步转到外交工作。她曾任国务院外事办公室亚非拉组副组长和中柬友协副会长。她曾多次随刘少奇主席、周总理和陈毅副总理兼外长出访亚欧国家；她自己也曾率中国妇女代表团访问过柬埔寨、越南等国。</p>	 
<p>1957年9月张茜同志出席了全国妇女第三次代表大会，并当选为全国妇联第三届执委会委员。1964年10月，张茜在湖北省当选为第三届全国人民代表大会代表。</p> 
<p>文化大革命期间，张茜同志和陈毅同志横遭迫害和打击，但他们在逆境中经受住了考验，表现出对党对人民的无限忠贞。1971年初陈毅同志患肠癌；于1972年1月6日逝世。70天后，张茜自己也发现患了肺癌，动了大手术。在极其残酷的现实面前，她以顽强的毅力和对陈毅深切的爱，将生命的余年全部用来整理陈毅的诗词和文稿。1973年春，她基本完成了陈毅诗词选集的整理编撰工作并将诗选打印成书。</p>
						<p>张茜同志在病中被中央军委任命为军事科学院外军部副部长。1974年春她在病危时曾写信给叶剑英副主席并转报毛主席、周总理，提出正式出版《陈毅诗词选集》的要求。同年3月20日，张茜同志逝世，享年52岁。</p>		
