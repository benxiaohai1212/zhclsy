						<p>八一建军节前夕，《陈毅诗选》推出中阿对照文版、中俄对照文版、中西对照文版，由外文出版社出版。此前，本书还出版了中英对照和中法对照文版。</P>
						<p>陈毅元帅兼有革命家、政治家、外交家和诗人的身份。本书遴选50余首陈毅元帅的诗歌，既反映了二十世纪从三十年代到六十年代中国的革命、建设历程，又生动再现了中国与世界各国的友好交往。选集中有陈毅的代表作《赣南游击词》、《梅岭三章》、《青松》，以及《赠缅甸友人》等名篇。这些诗篇中的“此去泉台招旧部，旌旗十万斩阎罗”；“要知松高洁，待到雪化时”等名句，被不少人引为座右铭；“我住江之头，君住江之尾。彼此情无限，共饮一江水”的咏叹，也为世人熟知。</p>
