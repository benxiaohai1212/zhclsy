							<h1>红色传记《三个新四军女兵的多彩人生》宣传推介会在南京举行</h1>
							<div class="parameters">摘自：《东进》通讯 2012.4.5第八期  肖茂修</div>
						</div>
						<div class="txtContent">
							<img src="../images/hszj.jpg" height="150px;" style="float:left;padding:10px;">
							<p>4月2日，中国新四军研究会、江苏省新四军研究会及北京新四军研究会在南京隆重举行《三个新四军女兵的多彩人生》宣传推介会。新四军老战士、华中抗日根据地老同志、革命前辈后代、党史军史专家学者、部队指战员以及青少年学生200多人参加。全国人大常委会委员、南京军区原司令员、中国新四军研究会会长朱文泉上将，江苏省人大常委会副主任、省新四军研究会副会长柏苏宁到会讲话。空军后勤部原副部长、北京新四军研究会副会长乔泰阳少将宣读了全国人大常委会副秘书长乔晓阳的书面讲话。中国中共文献研究会副会长陈小津，江苏省委宣传部副部长周琪，江苏省教育厅厅长沈健到会祝贺。</p>
							<p>红色传记《三个新四军女兵的多彩人生》的三位主人公分别是陈毅元帅的夫人张茜、开国上将叶飞的夫人王于畊、开国上将钟期光的夫人凌奔。这三位号称“美女、才女、侠女”的新四军女兵的传奇故事曾被拍成6集文献纪录片《新四军女兵》在中央电视台播出。本书日前由人民出版社出版，36万字。作者陈丹淮（陈毅、张茜之子）和叶葳崴（叶飞、王于畊之女）以革命后代的特有历史责任感及其独有的审美视角，深入挖掘研究史料，生动传神地描述了母亲们在战火中经受洗礼和成长的轨迹，展现了这三位新四军女兵多姿多彩的革命人生和浪漫情怀，为读者绘制了一幅讴歌革命先辈的感人画卷，为历史镌刻了一组瑰丽的传世群雕；同时，为我们进行光荣传统教育提供了一部生动教材，也为党史、军史人物研究作了新的探索。</p>
							<p>朱文泉在讲话中首先称赞该书宣传推介会本身就是一次很有意义的传统教育活动，该书的出版为广大读者奉献了一本宣传新四军光辉历史、讴歌革命母亲的好书。朱文泉在缅怀这三位新四军女兵的光辉业绩和崇高风范后指出，我们举办这个新书推介会，就是期望当代中国有更多的人，尤其是年轻人牢记过去，珍惜现在，创造未来，接好革命的班，真正像三位女兵那样，“踏着红色的理想而来，迎着灿烂的朝霞而进”，永葆红色江山代代相传！</p>
							<p>柏苏宁在讲话中首先赞扬作者的历史责任感和艰苦创作的功力。她指出，党的十八大即将召开，这是全党全国的一件大事。当前举国上下正在营造热烈喜庆的政治氛围，以迎接这个具有伟大历史意义的大会的召开。我们在这一强劲的春风中，隆重地向社会宣传推介这部应运而生的红色传记，无疑有着十分积极的意义。她指出，江苏人杰地灵，曾是新四军长期战斗生活的地区，新四军在江苏盐城重建军部，后又较长时间驻扎在黄花塘。因此，江苏的新四军史料资源特别丰富。我们要按照省委对研究会的指示要求，抓住机遇，发挥优势，加大挖掘力度，加大史料研究和成果转化力度，扎扎实实地为弘扬铁军精神，丰富新时期江苏精神，为推进我省“两个率先”服务。</p>
							<p>会上，作者陈丹淮少将介绍了《三个新四军女兵的多彩人生》的创作过程及本书特点。人民出版社总编辛广伟介绍了这本书出版的目的意义。著名军史家丁星、《大江南北》杂志总编唐功儒，书中女主人公凌奔的学生余志远，新四军政治部主任袁国平之子袁振威少将在会上发言。作者向部分部队、院校、图书馆、各地新四军纪念馆赠送了这部红色传记。粟裕之子粟寒生，叶飞子女叶小楠、叶葳崴、叶小琦，钟期光子女钟德苏、钟德浙、钟德沪等以及主办方负责同志徐承云、毋瞩远、王清葆、徐红、苏京等将军以及省委老干部局、省妇联等有关部门负责同志出席。宣传推介会在铁军艺术团和省新四军研究会后代艺术团的精彩演出中圆满结束。</p>
