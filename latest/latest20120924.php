							<h1>温家宝赴外交学院为周恩来同志和陈毅同志铜像揭幕并向师生们发表讲话</h1>
<div >来源：《人民日报》  2012年9月11日</div>
						</div>
						<div class="txtContent">
							<img src="../images/latest2012-10-16.jpg" height="150px;" style="float:left;padding:10px;">
							<p>国务院总理温家宝9月10日来到位于北京市昌平区的外交学院新校区，出席周恩来同志和陈毅同志铜像揭幕仪式。</p>
							<p>1955年周恩来倡议成立外交学院并亲自题写校牌，陈毅担任外交部长期间长期兼任学院院长。57年来，外交学院培养了包括近300位大使在内的大批外交人才，被誉为“中国外交官的摇篮”。</p>
							<p>铜像高4.5米，以1964年周恩来、陈毅一起出访南亚时所拍摄的照片为原型设计。</p>
							<p>温家宝为铜像揭幕，全场高唱国歌，学生们深情朗诵了自己创作的配乐诗歌《永远的怀念》，表达对老一辈革命家、外交家的深切缅怀和投身外交事业、报效祖国的坚定志向。</p>
							<p>温家宝在礼堂向师生们发表讲话。温家宝说，旧中国饱受屈辱，山河破碎，弱国无外交。中国政府和人民比任何人都珍惜来之不易的国家主权和民族尊严，即使在极其艰难困苦的情况下，也是铮铮铁骨。钓鱼岛是中国固有领土，在主权和领土问题上，中国政府和人民绝不会退让半步。</p>
							<p>温家宝说，中国走互利共赢的道路顺应了全球化的时代潮流，也是应对国际金融危机等挑战的唯一正确选择。近年来，我们倡议和推动了一系列重大合作举措，取得显著成效，为中国经济发展拓展了广阔空间，也为促进世界经济复苏和增长作出了贡献。</p>
							<p>温家宝指出，当前，国际热点问题成因更加复杂，变化更加快速，影响面更广。中国坚持独立自主的和平外交政策，根据事情的是非曲直独立作出判断，不与任何国家或国家集团结盟，积极参与国际安全合作，切实维护国家利益和世界人民福祉。</p>
							<p>温家宝说，作为最大的发展中国家，中国全面推进可持续发展的实践是人类历史上的创举，影响广泛而深远。我们将加强与世界各国的交流互鉴与合作，为推动全球可持续发展做出不懈努力。</p>
							<p>温家宝强调，我们要积极、主动、客观地向世界介绍中国，吸收借鉴人类一切优秀文明成果，尊重和维护文明多样性，扩大同各国的友好交往，增进相互理解。</p>
							<p>温家宝寄语外交学院要有一流师资、一流学科、一流理念，将一流人才输送给祖国外交事业，全体师生要保持和发扬优良传统，向新的更高目标不懈努力。</p>
							<p>温家宝最后说，毛泽东、周恩来、陈毅等老一辈革命家缔造了人民共和国，结束了近代中国一百多年屈辱的历史，也奠定了新中国外交的基础。我们要永远缅怀他们的丰功伟绩，学习、继承和发扬他们的思想品格和精神风范。经过几代人艰苦卓绝的奋斗，今天的中国已经巍然屹立在世界东方，受到国际社会前所未有的瞩目，必将发挥越来越重要的作用，中国外交有着广阔的天地，我们要抓住机遇，团结一致，乘势而上，为推进新时期中国外交的历史使命而努力奋斗。</p>
							<p>温家宝还会见了周恩来、陈毅的亲属和外交战线老同志，向他们和外交战线全体同志致以崇高敬意和亲切问候。</p>
							<p>中共中央政治局委员、中央书记处书记、中央宣传部部长刘云山，国务委员戴秉国，唐家璇同志，外交部部长杨洁篪，教育部部长袁贵仁，北京市代市长王安顺等出席了有关活动。</p>
