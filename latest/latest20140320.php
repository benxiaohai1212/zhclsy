							<div class="parameters">2014年3月20日</div>
						</div>
						<div class="txtContent" style="text-align:left;padding-top:10px;">
							<p>2014年3月20日是陈毅同志的夫人张茜逝世40周年纪念日。他们的子女陈昊苏、陈丹淮、陈珊珊以及外孙王新岳于3月18日护送张茜的灵骨至上海福寿园。19日上午张茜同志的灵骨在上海福寿园新四军广场落葬。陈毅长子陈昊苏发表讲话并赋诗一首，详见下方。北京青年报记者李俊兰跟踪了灵骨安葬全过程并作了详细报道，详见《北京青年报》2014年3月20日天天副刊《护送母亲张茜灵骨归队》。</p>
							<span style="font-style : italic;padding:20px 0px  0px 0px;">同志们，朋友们：</span>
							<p style="font-style : italic;">去年十月，在福寿园庄严的新四军广场路边，树立起我父母双亲并肩站立的塑像。那时我们兄妹与众多新四军老战士及他们的后代一起纪念新四军成立7 6周年。现在我们即将迎来抗日战争爆发77周年、新四军成立77周年纪念日。我们再次来到福寿园，向我们的父母双亲，众多的革命前辈致敬，献上我们真挚的怀念，崇高的敬意。</p>
							<p style="font-style : italic;">2014年3月20日是我们的母亲张茜逝世4 0周年。我和弟弟丹淮、妹妹珊珊一起，并代表临时出国在外的弟弟小鲁，怀着依恋护送我们母亲的灵骨从北京南下，来到上海福寿园落葬。我们经历了40乃至70年的漫长岁月，又一次回到江南故地，沐浴着岁岁年年不变的春光，品味着日升月恒老去的人生，那些峥嵘往事又在我们心头浮现，父母双亲年轻时代的容颜又回到我们的眼前。他们好像在向我们讲述江南抗战之春的历史。当国土沦丧、敌寇横行、人民受难的残酷年代，新四军的英雄儿女受命东进，挺进敌后，开展游击战争，收复大好河山，创建抗日游击区和中国人民自己当家做主的革命根据地。弯弓射日到江南，军民游击满南华，山呼抗战风雷动，十月红旗满天飞。前后三年，完成了共产党领导下的人民武装新四军在华中地区的战略展开，让江淮河汉连成一片，华中解放区横空出世，威震东亚。新中国在华中这块热土的牢固基础得以在战斗的岁月中奠定，中国革命向着胜利的目标前进，写下了精彩的篇章，灿烂辉煌。我们作为革命的后代，永远铭记前辈的战斗经历，他们奋斗、创业、奉献、牺牲，相伴着崇高的幸福、挚爱的欢乐、光荣的胜利、伟大的成功，开启民族复兴的中国梦踏上了万里征程，一往无前。</p>
							<p style="font-style : italic;">我们为时代的变迁而感慨，为社会的进步而欢欣。可以告慰双亲及众多铁军先辈的是，当年他们浴血奋战的这片热土，已经发生了沧海桑田般的神奇变化，从物质到精神，从城市到乡村，从土地到人民都完全改变了面貌。我们随处都可以见到社会主义现代化建设的磅礴进取，感受着先进的生产生活方式带给我们的种种便利，我们的祖国已经走进先进国家的行列，在世界事务中发挥重要的作用，我们正昂首阔步地向着更加文明富强民主和谐的方向进军。当然，在前进的路上还有不少困难，国家发展也带来一些消极的事物，例如环境受损、诚信缺失、天灾人祸等等，我们党正带领人民继续英勇奋斗，力求通过全面深化改革，推动社会进步，凝聚全体人民的意志并争取最大可能的国际国情，在不远的将来走出还不能令人满意的现状，迎接祖国更加美好的明天。我们要向先人的进取精神学习，对社会的持续进步付出我们的热情与努力，跟随党和人民在全面实现小康社会的奋斗中继续前进，用新的胜利告慰我们的先人和前辈。相信我们能够为国家进一步的发展作出贡献，把祖国的美丽春天演绎得更加辉煌，并且传之后代，永葆鲜明灿烂。</p>
							<p style="font-style : italic;">感谢同志们朋友们前来参加今天的仪式。我用一首《怀念亲人》的诗，向大家作深情的表白。</p>
