							<p>陈毅生于1901年8月26日,字仲弘，四川乐至人。</p>
							<p>1919年陈毅赴法国勤工俭学；1921年因参加学生爱国运动，被中法政府联合派人押送回国。1922年加入中国共产主义青年团。1923年加入中国共产党。以后在北方从事革命工作。1927年在武汉中央军校担任该校中共委员会书记。1927年南昌起义后，陈毅随起义部队南下。后与朱德一起率部上井冈山，与毛泽东率领的秋收起义部队会师，建立最早的红军武装第四军。</p>
							<p>土地革命战争时期（1927-1937），陈毅历任南昌起义部队二十五师七十三团党代表，工农革命军第一师党代表，中国工农红军第四军十二师党代表、师长，红四军政治部主任、军委书记，红六军、红三军政治委员，中共赣西南特委书记，红二十二军军长，江西军区总指挥兼政治委员，西方军总指挥，中华苏维埃共和国中央政府办事处主任。1934年红军开始长征后，留在江西苏区，领导了南方三年游击战争。</p>
			　　				<p>抗日战争时期（1937-1945），陈毅任新四军第一支队司令员，江南指挥部、苏北指挥部指挥，新四军代军长、军长。
							<p>解放战争时期（1945-1949），陈毅历任山东军区司令员，华东军区司令员，华东野战军司令员兼政治委员，中原军区和中原野战军副司令员，第三野战军司令员兼政治委员。 </p>
							<p>1949年新中国建立后，任华东军区司令员兼上海市市长，人民革命军事委员会副主席。1954年任国务院副总理。1955年被授予元帅军衔，获一级八一勋章、一级独立自由勋章和一级解放勋章。1958年兼任外交部部长。还曾任国务院外事办主任，外交学院院长，中国人民外交学会名誉会长，中共中央军委副主席，第一至三届国防委员会副主席，全国政协第三、四届副主席。是中共第七、八、九届中央委员、第八届中央政治局委员。1972年1月6日在北京逝世，享年71岁。 </p>
