							<h1>和张茜同学的日子</h1>
							<div class="parameters"></div>
						</div>
						<div class="txtContent">
							<p>1949年12月，我21岁，考入华东人民革命大学附设上海俄文学校，学习俄语。校址是老暨南大学的旧址，即现在上海外国语大学的校本部。校长是姜椿芳，教师大多是苏侨。当年第一期共招收三百多名学员，录取名单发表在1949年12月26日的《解放日报》上。</p>
							<p>那时，上海市长陈毅的夫人张茜，才27岁，也在我们学校学习俄语，成了我们的同学。名义上，她是学校的宣传股长。有一段时间她是我的同桌，在我的印象中，她是那样的青春秀美。碰到校长姜椿芳上翻译课，她是必到的，而且听得很认真，笔记也做得很仔细。每次被老师点到名，她就满脸绯红地站起来。真有点难以想象，她这样的性格，作为市长夫人，以后又作为外交部长的夫人，在各种外交场合，到底如何应对。她没有一点高官夫人的架子，和其他同学一样穿着列宁装，也是和我们一样在学校食堂用餐。</p>
							<p>那时大家都住校，学校生活团结友爱，丰富多彩。张茜为人随和，经常看到她和女同学们一起说说笑笑。星期六傍晚，陈毅市长常会亲自来接她。由于当时刚解放不久，敌特活动频繁，学校又地处偏远，市长的车来时，附近有一小段路要戒严，不过到了学校，就解除了。</p>
							<p>1950年除夕，学校搞迎新年联欢活动，内容有摸彩一项。同学们各拿一样东西出来做奖品。我正好摸到了张茜的笔记本。那是一个桔红色的硬面本子，翻开一看，里面是张茜娟秀的钢笔字：“一九五一年新年献礼！’希望收到这件礼物的同学好好使用它，用它记载你的工作经验和学习心得。并以此永志我们——在革命学校相与共处的革命青年们的团结和友爱。张茜赠于除夕。”几十年来这个笔记本一直妥善保存着，至今完好如初。</p>
							<p>1951年初，我们一期学员中的部分同学因工作需要提前毕业，我留校当了助教。不久张茜也随调任国务院副总理兼外交部长的陈毅去了北京。</p>
							<p>关于张茜的这段经历，见诸文字的小传很少提及，连晓植撰著、中国青年出版社1987年9月出版的长篇传记《张茜》中也只有一句话：“到上海后，张茜曾任上海俄语专科学校的宣传股长。”但晓植在“后记”中说：“很多人都以为张茜可能是出身子大家闺秀和最高学府。实际上她只是一位中等师范一年级的学生，以后的文化水平，完全是她刻苦自学得来的。”我想，这是对张茜的很确切的写照吧。</p>
